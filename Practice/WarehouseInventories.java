package Practice;

/**
 * Write a program that contains the following String variables: productName, itemCode,
 * and CustomerId. Initialize these variables with the values
 * “video-game”, “A101”, and “VD112” respectively.
 * The program should display the contents of these variables on the screen.
 * A
 * AE AEI AEIO AEIOU
 */
public class WarehouseInventories {
    public static void main(String[] args) {
        String productName = "Video-game";
        String itemCode = "A101";
        String customerId = "VD112";
        System.out.print("Product Name: " + productName + "\nItem Code: " + itemCode + "\nCustomerId: " + customerId);

        //Vowel Triangle Pattern
        System.out.print(" \n A" + "\n AE" + "\n AEIO" + "\n AEIOU");
    }
}
